/**
 * @param {number} num
 * @return {void}
 */
Robot.prototype.move = function (num) {
  let remainingSteps = num;
  let possibleStepsInCurrDir = num;

  while (remainingSteps > 0) {
    // console.log(num);
    if (remainingSteps > 2 * (this.width + this.height + 2)) {
      remainingSteps = remainingSteps % (2 * (this.width + this.height + 2));
			console.log('after remainder',remainingSteps)
    }

    if (this.direction == 0) {
      if (this.position[0] + num > this.width) {
        possibleStepsInCurrDir = this.width - (this.position[0] + num);
        if (possibleStepsInCurrDir < 0) {
          remainingSteps = Math.abs(possibleStepsInCurrDir);
          possibleStepsInCurrDir = num - remainingSteps;
        } else {
          remainingSteps = num - possibleStepsInCurrDir;
        }
      }else{remainingSteps = 0;possibleStepsInCurrDir= num}
      this.position = [
        this.position[0] + possibleStepsInCurrDir,
        this.position[1],
      ];
    } else if (this.direction == 90) {
      if (this.position[1] + num > this.height) {
        possibleStepsInCurrDir = this.height - (this.position[1] + num);
        if (possibleStepsInCurrDir < 0) {
          remainingSteps = Math.abs(possibleStepsInCurrDir);
          possibleStepsInCurrDir = num - remainingSteps;
        } else {
          remainingSteps = num - possibleStepsInCurrDir;
        }
      }else{remainingSteps = 0;possibleStepsInCurrDir= num}
      this.position = [
        this.position[0],
        this.position[1] + possibleStepsInCurrDir,
      ];
    } else if (this.direction == 180) {
      if (this.position[0] - num < 0) {
        possibleStepsInCurrDir = this.position[0] - num;
        if (possibleStepsInCurrDir < 0) {
          remainingSteps = Math.abs(possibleStepsInCurrDir);
          possibleStepsInCurrDir = num - remainingSteps;
        } else {
          remainingSteps = num - possibleStepsInCurrDir;
        }
      }else{remainingSteps = 0;possibleStepsInCurrDir= num}
      this.position = [
        this.position[0] - possibleStepsInCurrDir,
        this.position[1],
      ];
    } else if (this.direction == 270) {
      if (this.position[1] - num < 0) {
        possibleStepsInCurrDir = this.position[1] - num;
        if (possibleStepsInCurrDir < 0) {
          remainingSteps = Math.abs(possibleStepsInCurrDir);
          possibleStepsInCurrDir = num - remainingSteps;
        } else {
          remainingSteps = num - possibleStepsInCurrDir;
        }
      }else{remainingSteps = 0;possibleStepsInCurrDir= num}
      this.position = [
        this.position[0],
        this.position[1] - possibleStepsInCurrDir,
      ];
    }

    if (remainingSteps > 0)
      this.direction = this.direction == 270 ? 0 : this.direction + 90;
  }
  // console.log({ num, pos: this.position, dir: this.direction });
};

/**
 * @return {number[]}
 */
Robot.prototype.getPos = function () {
  return this.position;
};

/**
 * @return {string}
 */
Robot.prototype.getDir = function () {
  const dir = {
    0: "East",
    90: "North",
    180: "West",
    270: "South",
  };
  return dir[this.direction];
};

